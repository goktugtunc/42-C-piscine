int	ft_any(char **tab, int (*f)(char*))
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (tab[a])
	{
		while (tab[a][i])
		{
			if (f(tab[a][i]) != 0)
				return (1);
			i++;
		}
		a++;
	}
	return (0);
}
